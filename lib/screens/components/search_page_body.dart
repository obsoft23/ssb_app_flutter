class ItemTile extends StatelessWidget {
  final int itemNo;

  const ItemTile(
    this.itemNo,
  );

  @override
  Widget build(BuildContext context) {
    final Color color = Colors.primaries[itemNo % Colors.primaries.length];
    return Padding(
      padding: const EdgeInsets.all(8.0),
      child: ListTile(
        tileColor: color.withOpacity(0.3),
        onTap: () {
          print(searchResults[itemNo]);
          showMaterialModalBottomSheet(
            context: context,
            builder: (context) =>
                searchVocation(context, searchResults[itemNo]),
          );
        },
        title: Center(
          child: searchResults.isNotEmpty
              ? Text(
                  ' ${searchResults[itemNo]}',
                  key: Key('text_$itemNo'),
                )
              : null,
        ),
      ),
    );
  }

  searchVocation(context, title) {
    return Scaffold(
      appBar: AppBar(
        backgroundColor: Colors.white,
        elevation: 1,
        leading: GestureDetector(
          onTap: () {
            accs.clear();
            Navigator.pop(context);
          },
          child: Container(
            margin: EdgeInsets.only(left: 8),
            child: Center(
              child: Icon(Icons.arrow_back_ios, color: Colors.blue, size: 16),
            ),
          ),
        ),
        title: Text(
          '$title',
          style: TextStyle(
            color: Colors.black,
            fontSize: 16,
          ),
        ),
        actions: [
          IconButton(
            icon: Icon(Icons.search),
            color: Colors.blueAccent,
            onPressed: () {
              showSearch(context: context, delegate: MySearchDelegate());
            },
          ),
        ],
      ),
      body: StreamBuilder(
        stream: findUserRequest(title),
        builder: (context, snapshot) {
          if (snapshot.hasError) {
            return Center(
              child: Text("Error"),
            );
          } else if (snapshot.hasData) {
            return accs.isNotEmpty
                ? businessList()
                : Center(
                    child: Text(
                      "no nearby found",
                      style: TextStyle(fontSize: 20, color: Colors.red),
                    ),
                  );
          } else if (snapshot.connectionState == ConnectionState.waiting) {
            return Center(
                child: CircularProgressIndicator(
              color: Colors.green,
            ));
          } else {
            return Center(child: CircularProgressIndicator());
          }
        },
      ),
    );
  }
}

